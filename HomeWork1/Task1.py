wd = input('Enter the day of the week: ')
if wd == '1':
    print('Monday')
elif wd == '2':
    print('Tuesday')
elif wd == '3':
    print('Wednesday')
elif wd == '4':
    print('Thursday')
elif wd == '5':
    print('Friday')
elif wd == '6':
    print('Saturday')
elif wd == '7':
    print('Sunday')
else:
    print('Incorrect data')
